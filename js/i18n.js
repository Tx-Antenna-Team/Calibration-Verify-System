let currentLang = localStorage.getItem("cvsLang") || "th";

export function getLang() { return currentLang; }

export function t(key) {
  return translations[currentLang]?.[key] ?? translations.en?.[key] ?? key;
}

export function setLang(lang) {
  currentLang = lang;
  localStorage.setItem("cvsLang", lang);
  applyLanguage();
}

export function toggleLang() {
  setLang(currentLang === "th" ? "en" : "th");
}

export function applyLanguage() {
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    el.textContent = t(key);
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
    const key = el.getAttribute("data-i18n-placeholder");
    el.placeholder = t(key);
  });
  // Update lang toggle button label
  const langBtn = document.getElementById("lang-toggle-btn");
  if (langBtn) langBtn.textContent = currentLang === "th" ? "EN" : "TH";
  // Dispatch event so modules can react
  document.dispatchEvent(new CustomEvent("langChanged", { detail: currentLang }));
}
